package com.hr.model;

import javax.persistence.*;

@Entity
@Table(name = "holiday")
public class Holiday {
    int id;
    int year;
    int day;
    int month;
    String name;

    public Holiday() {
    }

    @Id
    @Column(name = "id")
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    @Basic
    @Column(name = "year")
    public int getYear() {
        return year;
    }

    public void setYear(int year) {
        this.year = year;
    }

    @Basic
    @Column(name = "day")
    public int getDay() {
        return day;
    }

    public void setDay(int day) {
        this.day = day;
    }

    @Basic
    @Column(name = "month")
    public int getMonth() {
        return month;
    }

    public void setMonth(int month) {
        this.month = month;
    }

    @Basic
    @Column(name = "name")
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return "Holiday{" +
                "id=" + id +
                ", year=" + year +
                ", day=" + day +
                ", month=" + month +
                ", name='" + name + '\'' +
                '}';
    }
}
